export default function HelpPage() {
  const [search, setSearch] = useState('');
  const [activeSection, setActiveSection] = useState<string>('intro');

  const filtered = useMemo(() => {
    if (!search.trim()) return sections;
    const q = search.toLowerCase();
    return sections.filter(
      (s) =>
        s.title.toLowerCase().includes(q) ||
        s.content.toLowerCase().includes(q) ||
        s.tags.some((t) => t.includes(q)) ||
        s.subsections.some((sub) => sub.title.toLowerCase().includes(q) || sub.text.toLowerCase().includes(q)),
    );
  }, [search]);

  const active = sections.find((s) => s.key === activeSection);

  return (
    <div>
      <Row gutter={[12, 12]}>
        <Col span={24}>
          <Space>
            <BookOutlined style={{ fontSize: 20 }} />
            <Title level={4} style={{ margin: 0 }}>Справка СПМО «Полёт» v5</Title>
          </Space>
        </Col>
      </Row>

      <Row gutter={[12, 12]} style={{ marginTop: 12 }}>
        <Col span={6}>
          <Input
            placeholder="Поиск по справке..."
            prefix={<SearchOutlined />}
            value={search}
            onChange={(e) => setSearch(e.target.value)}
            allowClear
            style={{ marginBottom: 8 }}
          />
          <div style={{ display: 'flex', flexDirection: 'column', gap: 2 }}>
            {filtered.map((s) => (
              <div
                key={s.key}
                onClick={() => setActiveSection(s.key)}
                style={{
                  padding: '6px 10px',
                  borderRadius: 4,
                  cursor: 'pointer',
                  background: activeSection === s.key ? '#e6f4ff' : 'transparent',
                  fontWeight: activeSection === s.key ? 600 : 400,
                }}
              >
                {s.title}
              </div>
            ))}
          </div>
          {filtered.length === 0 && (
            <Empty description="Ничего не найдено" image={Empty.PRESENTED_IMAGE_SIMPLE} />
          )}
        </Col>
        <Col span={18}>
          {active ? (
            <Card>
              <Title level={5}>{active.title}</Title>
              <Paragraph style={{ fontSize: 14, lineHeight: 1.8 }}>{active.content}</Paragraph>
              <Collapse
                items={active.subsections.map((sub, i) => ({
                  key: String(i),
                  label: sub.title,
                  children: <Text style={{ whiteSpace: 'pre-line', fontSize: 14, lineHeight: 1.8 }}>{sub.text}</Text>,
                }))}
                style={{ marginBottom: 12 }}
              />
              <Space style={{ marginTop: 8 }}>
                {active.tags.map((t) => <Tag key={t}>{t}</Tag>)}
              </Space>
              {active.reference && (
                <Paragraph type="secondary" style={{ marginTop: 12, fontSize: 12 }}>
                  {active.reference}
                </Paragraph>
              )}
            </Card>
          ) : (
            <Empty description="Выберите раздел слева" />
          )}
        </Col>
      </Row>
    </div>
  );
}
